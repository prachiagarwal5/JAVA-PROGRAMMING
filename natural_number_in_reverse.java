//FIRST N NATURAL NUMBER IN REVERSE ORDER
import java.util.Scanner;
public class Main{
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        System.out.println("enter the number");
        int n=sc.nextInt();
        for(int i=n;i>=1;i--){
            System.out.println(i);    
            }
         }
}
